'''

 PROBLEM:




 "On average across all industries, full-time employed Canadians work 39.5 hours per
 week. If the typical adult requires eight hours of sleep per day, what percentage of 
 the hours in a week are are left over for personal activities?"











 SOLUTION:

                                 hrs/week   work  sleep/week
 Number of free hours per week = (24 * 7) - 39.5 - (8 * 7)  =  72.5 hours per week
 
 Percentage = (72.5 / 168) * 100  =  ~ 43%

'''