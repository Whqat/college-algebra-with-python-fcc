'''

PROBLEM:

"If $9.99 is changed to $10.49, what is the percent change?"









SOLUTION:

Percentage of change = (Difference / Old value) * 100  (*100 converts it to percentage by getting the first 2 decimal places)

-> Difference = 10.49 - 9.99 = 0.50

-> Old value = 9.99

-> Percentage of change = (0.50 / 9.99) * 100  =  5%

'''